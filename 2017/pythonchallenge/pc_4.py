from urllib import urlopen
import re

url = "http://www.pythonchallenge.com/pc/def/linkedlist.php?nothing="
data = "8022"
result = ""
req = ""

reg = re.compile("and the next nothing is ([0-9]+)")

while True:
    req = urlopen(url + data).read()
    result = ''.join(reg.findall(req))
    if not result:
        break
    data = result
    print(data)

print(req)

